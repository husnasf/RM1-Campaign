document.getElementById("campaign").addEventListener("mouseover", campaignMouseOver);
document.getElementById("campaign").addEventListener("mouseout", campaignMouseOut);
    
    function campaignMouseOver() {
      document.getElementById("campaign").style.color = "rgb(123, 102, 255)";
    }
    
    function campaignMouseOut() {
      document.getElementById("campaign").style.color = "rgb(17, 63, 103)";
    }

